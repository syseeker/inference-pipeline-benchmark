#!/usr/bin/env python3
"""Render the SKILL.md files into per-agent doc formats.

One source of truth — `skills/<name>/SKILL.md` with a YAML frontmatter
block — fans out to:

  - AGENTS.md                  (repo root, picked up by Codex)
  - .cursor/rules/<name>.mdc   (one per skill, Cursor MDC format)

The Claude Code skill loader reads SKILL.md directly, so no rendering
needed for that path.

Run:
    python scripts/render_agent_docs.py [--check]

`--check` runs in dry-run / no-write mode and exits non-zero if the
rendered output would differ from what's on disk. Wire it into CI to
catch drift.
"""

from __future__ import annotations

import argparse
import re
import sys
from dataclasses import dataclass
from pathlib import Path

REPO_ROOT = Path(__file__).resolve().parents[1]
SKILLS_DIR = REPO_ROOT / "skills"
AGENTS_PATH = REPO_ROOT / "AGENTS.md"
CURSOR_RULES_DIR = REPO_ROOT / ".cursor" / "rules"

_FRONTMATTER = re.compile(r"^---\n(.*?)\n---\n(.*)$", re.DOTALL)


@dataclass
class Skill:
    name: str
    description: str
    body: str
    source_path: Path


def _parse_skill(path: Path) -> Skill:
    """Pull (name, description, body) from a SKILL.md.

    Frontmatter is YAML-shaped but we parse only `name:` and `description:`
    (single-line or `|` block scalar). Keeping it dependency-free —
    avoid pulling pyyaml into the renderer.
    """
    text = path.read_text()
    m = _FRONTMATTER.match(text)
    if not m:
        raise ValueError(f"{path}: missing or malformed frontmatter")
    head, body = m.group(1), m.group(2)

    name = ""
    desc_lines: list[str] = []
    in_desc_block = False
    for line in head.splitlines():
        if line.startswith("name:"):
            name = line.split(":", 1)[1].strip()
        elif line.startswith("description:"):
            rhs = line.split(":", 1)[1].strip()
            if rhs == "|":
                in_desc_block = True
            else:
                desc_lines.append(rhs)
        elif in_desc_block:
            # Continuation lines are indented; stop at the first non-indented
            # line. The block scalar's leading indent is whatever the first
            # indented line uses — we lstrip() each, which is fine for our
            # one-paragraph descriptions.
            if line.startswith((" ", "\t")):
                desc_lines.append(line.strip())
            else:
                in_desc_block = False
    if not name:
        raise ValueError(f"{path}: frontmatter missing `name:`")
    description = " ".join(desc_lines).strip()
    if not description:
        raise ValueError(f"{path}: frontmatter missing `description:`")
    return Skill(name=name, description=description, body=body.strip() + "\n", source_path=path)


def _render_agents_md(skills: list[Skill]) -> str:
    """Single combined doc for Codex (AGENTS.md at repo root)."""
    lines: list[str] = [
        "# Agents guide — inference-pipeline-benchmark",
        "",
        "Auto-generated from `skills/*/SKILL.md` by `scripts/render_agent_docs.py`.",
        "Do not edit this file directly — edit the SKILL.md sources and re-run the script.",
        "",
        "This repo benchmarks VLM and policy inference on NVIDIA GPUs. All",
        "agent-facing work goes through one CLI (`bench`) with a JSON status",
        "contract; the five sections below explain when to use each skill.",
        "",
        "## Skills index",
        "",
    ]
    for s in skills:
        lines.append(f"- [`{s.name}`](#{s.name}) — {s.description}")
    lines.append("")
    for s in skills:
        lines += ["---", "", f"## {s.name}", ""]
        lines.append(f"_Source: [skills/{s.name}/SKILL.md]({s.source_path.relative_to(REPO_ROOT)})_")
        lines.append("")
        # Drop the H1 from the body — AGENTS.md uses H2 per skill.
        body = re.sub(r"^# .+?\n+", "", s.body, count=1)
        lines.append(body.rstrip())
        lines.append("")
    return "\n".join(lines).rstrip() + "\n"


def _render_cursor_mdc(s: Skill) -> str:
    """One .mdc per skill (Cursor uses MDC: frontmatter + markdown body)."""
    return (
        "---\n"
        f"description: {s.description}\n"
        "alwaysApply: false\n"
        f"globs: [\"benchmarks/**\", \"scripts/**\", \"tests/smoke/scenarios*/**\"]\n"
        "---\n\n"
        + s.body.rstrip() + "\n"
    )


def _write_if_changed(path: Path, content: str, *, check: bool) -> bool:
    """Return True if path differs from `content`. In check mode we don't write."""
    existing = path.read_text() if path.exists() else ""
    if existing == content:
        return False
    if not check:
        path.parent.mkdir(parents=True, exist_ok=True)
        path.write_text(content)
    return True


def main() -> int:
    ap = argparse.ArgumentParser(description=__doc__)
    ap.add_argument("--check", action="store_true", help="Dry-run; exit non-zero on drift.")
    args = ap.parse_args()

    if not SKILLS_DIR.is_dir():
        print(f"no skills dir at {SKILLS_DIR}", file=sys.stderr)
        return 1
    skill_files = sorted(SKILLS_DIR.glob("*/SKILL.md"))
    if not skill_files:
        print(f"no SKILL.md files under {SKILLS_DIR}", file=sys.stderr)
        return 1
    skills = [_parse_skill(p) for p in skill_files]

    drift = False
    drift |= _write_if_changed(AGENTS_PATH, _render_agents_md(skills), check=args.check)
    for s in skills:
        mdc_path = CURSOR_RULES_DIR / f"{s.name}.mdc"
        drift |= _write_if_changed(mdc_path, _render_cursor_mdc(s), check=args.check)

    if args.check and drift:
        print("agent docs out of date — run: python scripts/render_agent_docs.py", file=sys.stderr)
        return 2
    print(f"rendered {len(skills)} skill(s) -> AGENTS.md + .cursor/rules/")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
